package Assignments;

public class Area_of_Rectangle 
{
	static void rectangle_area(int l,int b)
	{
		int area=l*b;
		System.out.println("Area of rectangle is "+area+" when length and breadth are "+l+" and "+b+" respectively");
	}
	public static void main(String[] args) 
	{
	rectangle_area(10, 45);	
	}

}
