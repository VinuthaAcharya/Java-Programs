package ClassAssignments;
//Store and Print 5 values in array
public class Array_Syntax1 
{
	public static void main(String[] args) 
	{
		String[] god=new String[5];
		god[0]="Ram";
		god[1]="Laxman";
		god[2]="Shiv";
		god[3]="Sita";
		god[4]="Pooja";
		for(int i=0;i<5;i++)
		{
			System.out.println(god[i]);
		}
	}

}
