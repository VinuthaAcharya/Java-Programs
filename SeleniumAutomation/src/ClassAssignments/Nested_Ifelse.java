package ClassAssignments;

public class Nested_Ifelse {

	public static void main(String[] args) 
	{
		int age=92;
		if(age>18)
		{
			if(age>60)
			{
				System.out.println("The person is senior citizen");
			}
			else
			{
				System.out.println("The person is young");
			}
		}
		else
		{
			System.out.println("Logout");
		}

	}

}
